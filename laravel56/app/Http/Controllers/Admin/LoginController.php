     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(AdminLoginPost $AdminLoginPost)
    {

        $data = DB::table('users')->where('username',$AdminLoginPost->input('username'))->first();
        $boolean = hash::check(trim($AdminLoginPost->password).$data->uuid,$data->password);
        if($boolean){
            session(['UserNameInfo'=>$data]);
            session(['AdminLoginToken'=>$data->uuid]);
        }
         return redirect(config('app.project'));
    }




